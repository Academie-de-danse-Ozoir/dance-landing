/**
 * Embarque server/pdf-assets dans un module TS importé par ticketPdf (aucun fs en prod).
 * Exécuter : node scripts/generate-pdf-assets.mjs
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const assetsDir = join(root, 'server', 'pdf-assets')
const outFile = join(root, 'server', 'utils', 'pdfAssets.bundle.ts')

const FILES = {
  afficheVerticalJpg: 'affiche_vertical.jpg',
  titleTtf: 'title.ttf',
  textTtf: 'text.ttf',
  brandLogoPng: 'brand-logo-light.png'
}

const publicFallbacks = {
  afficheVerticalJpg: join(root, 'public', 'images', 'affiche_vertical.jpg'),
  titleTtf: join(root, 'public', 'fonts', 'title.ttf'),
  textTtf: join(root, 'public', 'fonts', 'text.ttf'),
  brandLogoPng: join(root, 'public', 'brand-logo-light.png')
}

function readAsset(key, fileName) {
  const primary = join(assetsDir, fileName)
  if (existsSync(primary)) return readFileSync(primary)
  const fallback = publicFallbacks[key]
  if (fallback && existsSync(fallback)) return readFileSync(fallback)
  throw new Error(`[generate-pdf-assets] Missing asset: ${fileName} (checked ${primary})`)
}

const entries = Object.entries(FILES).map(([key, fileName]) => {
  const buf = readAsset(key, fileName)
  return `  ${key}: Buffer.from(${JSON.stringify(buf.toString('base64'))}, 'base64')`
})

const source = `/* eslint-disable */
// AUTO-GENERATED by scripts/generate-pdf-assets.mjs — do not edit by hand.
// Regenerate: npm run generate:pdf-assets

export const pdfAssetsBundle = {
${entries.join(',\n')}
} as const

export type PdfAssetsBundle = typeof pdfAssetsBundle
`

writeFileSync(outFile, source, 'utf8')
console.log(`[generate-pdf-assets] Wrote ${outFile}`)
