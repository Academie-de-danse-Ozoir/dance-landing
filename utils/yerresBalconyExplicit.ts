/** `null` = emplacement X (vide). Rangée **O** incluse (parterre). */
export function balconyExplicitSequence(rowLetter: string): (string | null)[] | null {
  const line = LINES[rowLetter.toUpperCase()]
  if (!line) return null
  return line
    .split(/\s+/)
    .filter(Boolean)
    .map((t) => {
      const u = t.toUpperCase()
      if (u === 'X') return null
      return u
    })
}
